#!/usr/bin/env node
/**
 * Reads:  github/gitignore master repo (root + Global/) via shallow git clone.
 * Writes: src/data/gitignore-templates.ts — ships the bundle so runtime has zero network I/O.
 * Run:    node scripts/sync-gitignore-templates.mjs
 *
 * Source license: CC0-1.0 (https://github.com/github/gitignore/blob/main/LICENSE).
 * Refresh cadence is manual; commit the regenerated TS file.
 *
 * The community/ subdirectory is intentionally excluded — quality varies and
 * the marginal coverage does not justify the bundle weight.
 */

import { execSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const tmpRoot = path.join(projectRoot, '.tmp');
const tmpDir = path.join(tmpRoot, 'gitignore-source');
const outFile = path.join(projectRoot, 'src', 'data', 'gitignore-templates.ts');

function slugify(name) {
  return name
    .replace(/\+/g, 'p')
    .replace(/#/g, 'sharp')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/[^a-zA-Z0-9]+/g, '-')
    .toLowerCase()
    .replace(/^-+|-+$/g, '');
}

function collectFrom(dir, group) {
  const files = fs.readdirSync(dir).filter((f) => f.endsWith('.gitignore'));
  const prefix = group === 'globals' ? 'g-' : 'l-';
  return files.map((f) => {
    const name = f.replace(/\.gitignore$/, '');
    const content = fs.readFileSync(path.join(dir, f), 'utf8').replace(/\r\n/g, '\n').trim();
    return { id: prefix + slugify(name), name, group, content };
  });
}

if (fs.existsSync(tmpDir)) {
  fs.rmSync(tmpDir, { recursive: true, force: true });
}
fs.mkdirSync(tmpRoot, { recursive: true });

console.log('Cloning github/gitignore (shallow)...');
execSync(`git clone --depth 1 --quiet https://github.com/github/gitignore.git "${tmpDir}"`, {
  stdio: 'inherit',
});

const root = collectFrom(tmpDir, 'languages');
const globals = collectFrom(path.join(tmpDir, 'Global'), 'globals');
const all = [...root, ...globals].sort((a, b) => a.name.localeCompare(b.name));

const seen = new Set();
for (const t of all) {
  if (seen.has(t.id)) {
    throw new Error(`Duplicate slug "${t.id}" generated from template "${t.name}"`);
  }
  seen.add(t.id);
}

const tsHeader = `/**
 * Auto-generated by scripts/sync-gitignore-templates.mjs
 * Source: https://github.com/github/gitignore (CC0-1.0)
 * Refresh: \`node scripts/sync-gitignore-templates.mjs\` then commit the result.
 *
 * Includes the repo root (programming languages and frameworks) plus Global/
 * (IDEs, OS, tools). community/ is intentionally excluded.
 */

export type GitignoreGroup = 'languages' | 'globals';

export interface GitignoreTemplate {
  id: string;
  name: string;
  group: GitignoreGroup;
  content: string;
}

`;

const tsBody = `export const gitignoreTemplates: GitignoreTemplate[] = ${JSON.stringify(all, null, 2)};\n`;

fs.writeFileSync(outFile, tsHeader + tsBody);
console.log(`Wrote ${all.length} templates to ${path.relative(projectRoot, outFile)}`);
console.log(`  Languages & frameworks: ${root.length}`);
console.log(`  Globals (IDE/OS/tools): ${globals.length}`);

fs.rmSync(tmpRoot, { recursive: true, force: true });
